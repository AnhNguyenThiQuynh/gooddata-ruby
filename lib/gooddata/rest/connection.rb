# encoding: utf-8

module GoodData
  module Rest
    # Wrapper of low-level HTTP/REST client/library
    class Connection
      def initialize(opts)
      end

      # HTTP DELETE
      #
      # @param uri [String] Target URI
      def delete(uri)
      end

      # HTTP GET
      #
      # @param uri [String] Target URI
      def get(uri)
      end

      # HTTP PUT
      #
      # @param uri [String] Target URI
      def put(uri)
      end

      # HTTP POST
      #
      # @param uri [String] Target URI
      def post(uri)
      end
    end
  end
end
